import java.io.*;
import java.util.*;
public class Operators
{
    public static void main(String args[])
    {
        int num1 = 100;
        int num2 = 20;

        System.out.println("Num1 + Num2 = "+(num1+num2));

        System.out.println("Num1 - Num2 = "+(num1-num2));

        System.out.println("Num1 * Num2 = "+(num1*num2));

        System.out.println("Num1 / Num2 = "+(num1/num2));

        System.out.println("Num1 % Num2 = "+(num1%num2));
    }

}